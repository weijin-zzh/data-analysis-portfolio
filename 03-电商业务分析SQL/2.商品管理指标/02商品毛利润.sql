# - 定义：商品毛利（售价 - 成本价）与售价的比例，反映商品的盈利能力（剔除营销、仓储等费用前的利润空间）。
# - 核心作用：评估商品定价合理性，筛选高毛利商品重点推广，是利润分析的基础指标。
# - 计算逻辑：
# 数据来源：product_detail表
# 公式：毛利率 = [（售价 - 成本价）/ 售价] × 100%（可按商品、分类、品牌聚合计算）
# 涉及字段：product_detail.price（售价）、product_detail.cost_price（成本价）。
# - 结果字段示例：
#   - 商品分类：美妆
#   - 平均售价：150 元
#   - 平均成本价：60 元
#   - 分类毛利率：60%
# - 理想结果：
#   - 整体毛利率≥30%（覆盖运营成本后仍有利润空间），且各分类毛利率差异不大：最高与最低分类的毛利率比值＜3 倍（如美妆 60% vs 日用品 30%，比值 2 倍）。
#   - 高毛利商品（≥40%）销售额占比≥40%（支撑整体利润）。
# - 业务解读：
#   - 若分类毛利率＜20%（如日用品 25% 接近临界值）：优化采购成本（如批量采购议价）或调整定价（如提升溢价空间，强调 “天然成分” 等卖点）。
#   - 若高毛利商品占比＜30%：加大其曝光（如首页推荐、搜索加权），或开发更多高毛利品类。
# 3. SKU 销售占比
select
    brand,
    round(avg(price-cost_price / price*100 ),2) as gross_margin_rate
from product_detail
group by brand
order by  gross_margin_rate desc;
